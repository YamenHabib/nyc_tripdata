icons = [blueIcon, greenIcon, redIcon, yellowIcon, goldIcon, violetIcon];


var source = new EventSource('/taxi2'); 
mapMarkers = [];


source.addEventListener('message', function(e){
    
    for (var i = 0; i < mapMarkers.length; i++) {
        mymap.removeLayer(mapMarkers[i]);
      }
    data = JSON.parse(e.data)

    for( var i =0; i < data.length; i++) {
        markerStart = L.marker([data[i].Start_Lat, data[i].Start_Lon], {icon: icons[i]}).addTo(mymap);
        markerEnd = L.marker([data[i].End_Lat, data[i].End_Lon], {icon: icons[i]}).addTo(mymap);
        mapMarkers.push(markerStart);
        mapMarkers.push(markerEnd);
    }
}, false);

  

  